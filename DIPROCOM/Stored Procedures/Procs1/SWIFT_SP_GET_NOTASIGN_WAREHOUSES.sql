-- Ejemplo de Ejecucion:				
				select [DIPROCOM].SWIFT_SP_GET_NOTASIGN_WAREHOUSES
*/
CREATE PROCEDURE [DIPROCOM].SWIFT_SP_GET_NOTASIGN_WAREHOUSES

@LOGIN varchar(50)
AS
BEGIN

DECLARE @TOTALASSIGN INT 

SELECT @TOTALASSIGN =  COUNT(*) FROM [DIPROCOM].[SWIFT_WAREHOUSES] 
	
			WHERE [DIPROCOM].[SWIFT_WAREHOUSES].[CODE_WAREHOUSE] NOT IN 
			
			(SELECT DEFAULT_WAREHOUSE FROM  [DIPROCOM].[USERS] WHERE [DIPROCOM].[USERS].CORRELATIVE != @LOGIN OR [DIPROCOM].[USERS].LOGIN = NULL)


IF @TOTALASSIGN > 0
 BEGIN

		    SELECT  * FROM [DIPROCOM].[SWIFT_WAREHOUSES] 
	
			WHERE [DIPROCOM].[SWIFT_WAREHOUSES].[CODE_WAREHOUSE] NOT IN 
			
			(SELECT DEFAULT_WAREHOUSE FROM  [DIPROCOM].[USERS] WHERE [DIPROCOM].[USERS].CORRELATIVE != @LOGIN OR [DIPROCOM].[USERS].LOGIN = NULL)

		UNION	
			SELECT  * FROM [DIPROCOM].[SWIFT_WAREHOUSES] 
	
			WHERE [DIPROCOM].[SWIFT_WAREHOUSES].[CODE_WAREHOUSE]  IN 
			
			(SELECT DEFAULT_WAREHOUSE FROM  [DIPROCOM].[USERS] WHERE [DIPROCOM].[USERS].CORRELATIVE = @LOGIN)
			
		ORDER BY [DIPROCOM].[SWIFT_WAREHOUSES].[DESCRIPTION_WAREHOUSE]			
			
END


IF @TOTALASSIGN = 0
 BEGIN

		    SELECT  * FROM [DIPROCOM].[SWIFT_WAREHOUSES] 
	
END


END
