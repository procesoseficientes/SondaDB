	--	,[SW].[STATUS_WAREHOUSE]
	--	,[SW].[LAST_UPDATE]
	--	,[SW].[LAST_UPDATE_BY]
	--	,[SW].[IS_EXTERNAL]
	--	,[SW].[BARCODE_WAREHOUSE]
	--	,[SW].[SHORT_DESCRIPTION_WAREHOUSE]
	--	,[SW].[TYPE_WAREHOUSE]
	--	,[SW].[ERP_WAREHOUSE]
	--	,[SW].[ADDRESS_WAREHOUSE]
	--	,[SW].[GPS_WAREHOUSE]
	--FROM [PACASA].[SWIFT_WAREHOUSES] [SW]
	--LEFT JOIN [PACASA].[USERS] [U] ON (
	--	[U].[DEFAULT_WAREHOUSE] = [SW].[CODE_WAREHOUSE]
	--)
