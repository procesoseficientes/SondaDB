CREATE PROCEDURE [PACASA].[SWIFT_SP_GET_UNASSIGNED_SALE_WAREHOUSE_BY_DISTRIBUTION_CENTER_AND_LOGIN](
	@LOGIN VARCHAR(50) = NULL	
	,@DISTRIBUTION_CENTER_ID INT
)
AS
BEGIN
	SET NOCOUNT ON;
	--
	DECLARE @CORRELATIVE INT
	--
	SELECT @CORRELATIVE = CORRELATIVE FROM [PACASA].[USERS] WHERE @LOGIN = [LOGIN]
	--
	SELECT DISTINCT
		[SW].[WAREHOUSE]
		,[SW].[CODE_WAREHOUSE]
		,[SW].[DESCRIPTION_WAREHOUSE]
		,[SW].[WEATHER_WAREHOUSE]
		,[SW].[STATUS_WAREHOUSE]
		,[SW].[LAST_UPDATE]
		,[SW].[LAST_UPDATE_BY]
		,[SW].[IS_EXTERNAL]
		,[SW].[BARCODE_WAREHOUSE]
		,[SW].[SHORT_DESCRIPTION_WAREHOUSE]
		,[SW].[TYPE_WAREHOUSE]
		,[SW].[ERP_WAREHOUSE]
		,[SW].[ADDRESS_WAREHOUSE]
		,[SW].[GPS_WAREHOUSE] 
	FROM [PACASA].[SWIFT_WAREHOUSES] [SW]
	INNER JOIN [PACASA].[SWIFT_WAREHOUSE_X_DISTRIBUTION_CENTER] [WDC] ON (
		[WDC].[CODE_WAREHOUSE] = [SW].[CODE_WAREHOUSE]
	)
	LEFT JOIN [PACASA].[USERS] [U] ON (
		[U].[DEFAULT_WAREHOUSE] = [WDC].[CODE_WAREHOUSE]
	)
	WHERE [WDC].[DISTRIBUTION_CENTER_ID] = 1
	--	AND	([U].[DEFAULT_WAREHOUSE] IS NULL OR [U].[USER_TYPE]='PRE')

