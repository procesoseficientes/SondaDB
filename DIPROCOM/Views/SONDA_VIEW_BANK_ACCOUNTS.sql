CREATE VIEW [PACASA].[SONDA_VIEW_BANK_ACCOUNTS]
AS
	SELECT
		[VALUE_TEXT_CLASSIFICATION] + [NAME_CLASSIFICATION] AS [ACCOUNT_BASE]
		,[NAME_CLASSIFICATION] [ACCOUNT_NAME]
		,[VALUE_TEXT_CLASSIFICATION] [ACCOUNT_NUMBER]
		,[NAME_CLASSIFICATION] [ACCOUNT_BANK]
	FROM
		[PACASA].[SWIFT_CLASSIFICATION]
	WHERE
		[GROUP_CLASSIFICATION] = 'BANK_ACCOUNTS';