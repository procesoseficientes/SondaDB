CREATE PROCEDURE [SONDA].[BULK_DATA_SP_IMPORT_SELLER_INFO]
AS
BEGIN
  SET NOCOUNT ON;
  --
  MERGE SWIFT_EXPRESS.[SONDA].[USERS] AS TRG
  USING (SELECT
		 CONVERT(VARCHAR(50),[EVS].[SELLER_CODE]) [SELLER_CODE],
		 [EVS].[SELLER_NAME],
		 [EVS].[SELLER_TYPE],
		 [EVS].[SELLER_TYPE_DESCRIPTION],
		 [EVS].[CODE_ROUTE],
		 [EVS].[NAME_ROUTE],
		 [EVS].[CODE_WAREHOUSE],
		 [EVS].[NAME_WAREHOUSE]
    FROM [SWIFT_INTERFACES_ONLINE].[SONDA].[ERP_VIEW_SELLER] EVS ) AS SRC
  ON TRG.[RELATED_SELLER] COLLATE database_default = SRC.[SELLER_CODE]
  WHEN MATCHED
    THEN UPDATE
      SET [SELLER_ROUTE] = [SRC].[CODE_ROUTE]
         ,[USER_TYPE] = [SRC].[SELLER_TYPE_DESCRIPTION]
         ,[DEFAULT_WAREHOUSE] = [SRC].[CODE_WAREHOUSE]
		 ,[PRESALE_WAREHOUSE] = [SRC].[CODE_WAREHOUSE]
		 ,[ROUTE_RETURN_WAREHOUSE] = [SRC].[CODE_WAREHOUSE];
	--
	MERGE SWIFT_EXPRESS.[dbo].[SWIFT_USER] AS TRG
  USING (SELECT
		 CONVERT(VARCHAR(50),[EVS].[SELLER_CODE]) [SELLER_CODE],
		 [EVS].[SELLER_NAME],
		 [EVS].[SELLER_TYPE],
		 [EVS].[SELLER_TYPE_DESCRIPTION],
		 [EVS].[CODE_ROUTE],
		 [EVS].[NAME_ROUTE],
		 [EVS].[CODE_WAREHOUSE],
		 [EVS].[NAME_WAREHOUSE]
    FROM [SWIFT_INTERFACES_ONLINE].[SONDA].[ERP_VIEW_SELLER] EVS ) AS SRC
  ON TRG.[RELATED_SELLER] COLLATE database_default = SRC.[SELLER_CODE]
  WHEN MATCHED
    THEN UPDATE
      SET [SELLER_ROUTE] = [SRC].[CODE_ROUTE]
         ,[USER_TYPE] = [SRC].[SELLER_TYPE_DESCRIPTION]
         ,[DEFAULT_WAREHOUSE] = [SRC].[CODE_WAREHOUSE]
		 ,[PRESALE_WAREHOUSE] = [SRC].[CODE_WAREHOUSE]
		 ,[ROUTE_RETURN_WAREHOUSE] = [SRC].[CODE_WAREHOUSE];
END